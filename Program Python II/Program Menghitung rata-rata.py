# Menghitung rata-rata
# Program Dibawah ini dibuat untuk Menghitung Rata Rata

x =int(input("Masukan Angka :"))
y =int(input("Masukan Angka :"))
z =int(input("Masukan Angka :"))

a = ( x+y+z ) / 5
print("hasil perhitungannya : %i" %a)

# Program yang saya buat menggunakan tipe data integer(%i)
# saat semua inputan x,y,z dan a itu di eksekusi
